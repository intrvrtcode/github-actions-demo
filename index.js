function add(a, b) {
  return a + b;
}

function subtract(a, b) {
  return a - b;
}

function multiply(a, b) {
  return a * b;
}

function divide(a, b) {
  return a / b;
}

function pow(a, b) {
  return a ** b;
}

function mod(a, b) {
  return a % b;
}

function square(a) {
  return a * a;
}

module.exports = { add, subtract, multiply, divide, pow, mod, square };
